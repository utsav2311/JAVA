public class switch65a {
      public static void main(String[] args) {
            int n = 2;//float is not used here(byte,short,string are used)

            switch (n) 
            {
                  case 1:
                        System.out.println("One");
                        break;
                  case 2:
                        System.out.println("Two");
                        break;
                  case 3:
                        System.out.println("Three");
                        break;
                  default:
                        System.out.println("Invalid");
                        break;
            }
      }
}
